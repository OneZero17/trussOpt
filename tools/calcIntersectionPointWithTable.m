function angle = calcIntersectionPointWithTable(x0,y0)
    theta1 = 2*atan((2*(- 10000*y0^2 - 578551*2^(1/2) + 17852498)^(1/2) - 4223*2^(1/2) + 274)/(200*y0 + 4223*2^(1/2)));
    theta2 = -2*atan((4223*2^(1/2) + 2*(- 10000*y0^2 - 578551*2^(1/2) + 17852498)^(1/2) - 274)/(200*y0 + 4223*2^(1/2)));
    theta3 = 2*atan((100*y0 - 100*x0 + (20000*x0^2 - 2400000*x0 + 20000*y0^2 + 71981231)^(1/2) + 6000)/(100*x0 + 100*y0 - 5863));
    theta4 = -2*atan((100*x0 - 100*y0 + (20000*x0^2 - 2400000*x0 + 20000*y0^2 + 71981231)^(1/2) - 6000)/(100*x0 + 100*y0 - 5863));
    theta = [theta1; theta2; theta3; theta4];

    theta = theta(theta<pi/2 & theta>-pi/4);
    angle = min(theta);
end

